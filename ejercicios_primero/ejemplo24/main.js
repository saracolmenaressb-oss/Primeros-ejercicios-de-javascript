let valor = "Hola";

console.log("Valor:", valor);
console.log("Tipo:", typeof valor);
console.log("Es número:", typeof valor === "number");
console.log("Es NaN:", Number.isNaN(valor));
console.log("Es texto:", typeof valor === "string");
/* 
Mostré información de una variable:
typeof → tipo de dato
comparación → saber si es número o texto
Number.isNaN() → verificar si es NaN
*/