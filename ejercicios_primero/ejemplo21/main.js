let texto = "   Hola JavaScript   ";

console.log("Longitud:", texto.length);
console.log("Contiene 'JavaScript':", texto.includes("JavaScript"));
console.log("Comienza con 'Hola':", texto.trim().startsWith("Hola"));
console.log("Mayúsculas:", texto.toUpperCase());
console.log("Sin espacios:", texto.trim());
/* 
Usé varios métodos de strings:
.length → cuenta caracteres
.includes() → busca texto
.startsWith() → verifica inicio
.toUpperCase() → convierte
.trim() → quita espacios
*/